function syncLangToggle(lang) {
  const flag = document.getElementById('lang-flag');
  const toggle = document.getElementById('lang-toggle');
  if (!flag || !toggle) return;

  const isFrench = lang === 'fr';
  flag.src = isFrench ? 'img/icon_map-fr.svg' : 'img/icon_map-gb.svg';
  flag.alt = isFrench ? 'French' : 'English';
  toggle.setAttribute('aria-label', isFrench ? 'Switch language to English' : 'Passer en francais');
}

function applyLang(lang){
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.getAttribute('data-i18n');
    if(translations[lang] && translations[lang][key]) {
      if (el.hasAttribute('data-i18n-html')) {
        el.innerHTML = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });
  localStorage.setItem('site-lang', lang);
  const sel = document.getElementById('lang-select');
  if(sel) sel.value = lang;
  syncLangToggle(lang);
}

document.addEventListener('DOMContentLoaded', ()=>{
  const saved = localStorage.getItem('site-lang') || 'en';
  applyLang(saved);
  const backToTopButton = document.querySelector('.back-to-top');

  function animateScrollTo(top, duration = 1100) {
    const start = window.pageYOffset || document.documentElement.scrollTop;
    const delta = top - start;
    const startTime = performance.now();

    function easeInOutCubic(t) {
      return t < 0.5 ? 4 * t * t * t : 1 - Math.pow(-2 * t + 2, 3) / 2;
    }

    function step(now) {
      const elapsed = now - startTime;
      const progress = Math.min(elapsed / duration, 1);
      const eased = easeInOutCubic(progress);
      window.scrollTo(0, start + delta * eased);
      if (progress < 1) {
        window.requestAnimationFrame(step);
      }
    }

    window.requestAnimationFrame(step);
  }

  function updateBackToTopVisibility() {
    if (!backToTopButton) return;
    const shouldShow = (window.pageYOffset || document.documentElement.scrollTop) > 260
      && !document.body.classList.contains('menu-open');
    backToTopButton.classList.toggle('is-visible', shouldShow);
  }

  if (backToTopButton) {
    backToTopButton.addEventListener('click', () => animateScrollTo(0));
    updateBackToTopVisibility();
    window.addEventListener('scroll', updateBackToTopVisibility, { passive: true });
  }

  const sel = document.getElementById('lang-select');
  if(sel){
    sel.addEventListener('change', ()=>applyLang(sel.value));
  }
  const langToggle = document.getElementById('lang-toggle');
  if (langToggle && sel) {
    langToggle.addEventListener('click', () => {
      const nextLang = sel.value === 'fr' ? 'en' : 'fr';
      sel.value = nextLang;
      applyLang(nextLang);
    });
  }

  const siteHeader = document.querySelector('.site-header');
  const menuToggle = document.querySelector('.menu-toggle');
  const nav = document.getElementById('site-nav');

  function clearMenuLockStyles() {
    document.documentElement.classList.remove('menu-open');
    document.body.classList.remove('menu-open');
    updateBackToTopVisibility();
  }

  function smoothScrollToTarget(target) {
    const header = document.querySelector('.site-header');
    const headerHeight = header ? header.offsetHeight : 0;
    const targetRect = target.getBoundingClientRect();
    const scrollTop = window.pageYOffset || document.documentElement.scrollTop;
    const offset = targetRect.top + scrollTop - headerHeight - 8;
    window.scrollTo({ top: offset, behavior: 'smooth' });
  }

  function unlockMenu() {
    clearMenuLockStyles();

    if (siteHeader) {
      siteHeader.classList.remove('nav-open');
    }

    if (menuToggle) {
      menuToggle.setAttribute('aria-expanded', 'false');
      menuToggle.setAttribute('aria-label', 'Open menu');
    }
  }

  function lockMenu() {
    document.documentElement.classList.add('menu-open');
    document.body.classList.add('menu-open');

    if (siteHeader) {
      siteHeader.classList.add('nav-open');
    }

    if (menuToggle) {
      menuToggle.setAttribute('aria-expanded', 'true');
      menuToggle.setAttribute('aria-label', 'Close menu');
    }
  }

  function setMenuOpen(isOpen) {
    if (!siteHeader || !menuToggle) return;
    if (window.innerWidth > 900) isOpen = false;

    if (isOpen) {
      if (!document.body.classList.contains('menu-open')) {
        lockMenu();
      }
      return;
    }

    if (document.body.classList.contains('menu-open')) {
      unlockMenu();
    }
  }

  if (menuToggle && nav) {
    menuToggle.addEventListener('click', () => {
      const isOpen = siteHeader.classList.contains('nav-open');
      setMenuOpen(!isOpen);
    });

    window.addEventListener('resize', () => {
      if (window.innerWidth > 900) setMenuOpen(false);
    });
  }

  // Smooth scroll for anchor links
  document.querySelectorAll('a[href^="#"]').forEach(link => {
      link.addEventListener('click', function(e) {
        const targetId = this.getAttribute('href').slice(1);
        const target = document.getElementById(targetId);
        if(target) {
          e.preventDefault();
          const shouldCloseMenu = document.body.classList.contains('menu-open') && this.closest('#site-nav');
          if (shouldCloseMenu) {
            setMenuOpen(false);
            requestAnimationFrame(() => {
              requestAnimationFrame(() => smoothScrollToTarget(target));
            });
            return;
          }

          smoothScrollToTarget(target);
        }
      });
  });
});
